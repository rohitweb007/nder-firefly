        <div class="span8">
          <p>
            So here's the idea:
          </p>
          <p>
            Every month I make a certain amount of money (my salary). So far it's easy enough. But
            that amount reaches me around the 23rd, which is annoying because it changes all the time. Some
            months it's the 24th, then the 22nd, etc. Annoying. Also, this doesn't match the month; it's somewhere
            at two-thirds.
          </p>
          <p>
            So I've started over. At the very moment my salary is transferred to me, I move it to my savings account.
            All of it. It's <em>gone</em>.
          </p>
          <p>
            At the start of each month I take a set amount of money from that salary, and transfer it back to my
            checking account. It's always the same amount and it matches (more or less) what I intend to spend that
            month. Let's say it's &euro; 1000,-. That is the amount of money I can spend that month.
          </p>
          <h4>In short</h4>
          <ul>
            <li>Each month on the 1st, my checking account's balance is exactly &euro; 1000,-.</li>
            <li>Any leftovers from the previous month have been transferred away: I save this amount.</li>
            <li>During the month I spend the &euro; 1000,-:
              <ul>
                <li>Recurring costs: rent, insurance, bills</li>
                <li>Variable costs: food, drinks, anything.</li>
                <li>Savings: to pay debts, holidays, a little extra, emergency funds.</li>
              </ul>
            </li>
          </ul>
          <p>
            This process means it's downhill from the 1st up until the 30st (or 31st) of each month. The fact
            that my salary arrives on the 23rd doesn't mean anything: it's stacked temporarily on my savings account
            the moment I get it.
          </p>
          <p>
            It also means that it doesn't matter if I make more or less in a month. The set start amount for each month
            remains the same. I can jiggle it around each month but the idea remains the same.
          </p>
          <h4>Summary</h4>
          <p>
            Start each month with a set amount. Use it all for savings and spending. Don't substract savings in advance.
            Move away your salary when you get it. Don't cheat by taking money from the "next" month.
          </p>
        </div>
